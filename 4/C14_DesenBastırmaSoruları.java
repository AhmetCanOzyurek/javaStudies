package day04__Stringmanupilation_ForLoops;

public class C14_DesenBastırmaSoruları {
    public static void main(String[] args) {
        // 8 ' e 5 yıldızlı bir dikdörtgen yazdırın

        for (int i = 1; i <=5 ; i++) {
            for (int j = 1; j <=8 ; j++) {
                System.out.print("* ");
            }
            System.out.println();
        }
    }

}
